package dao;

public class AdvertiseDTO {
	private int adno;
	private String adid;
	private String adsubject;
	private String msg;
	private String adtime;
	private int hit;
	private String ppc;
	private String url;
	private String maj_category;
	private String sub_category;
	private String img;
	public String getAdtime() {
		return adtime;
	}
	public void setAdtime(String adtime) {
		this.adtime = adtime;
	}
	public int getAdno() {
		return adno;
	}
	public void setAdno(int adno) {
		this.adno = adno;
	}
	public String getAdid() {
		return adid;
	}
	public void setAdid(String adid) {
		this.adid = adid;
	}
	public String getAdsubject() {
		return adsubject;
	}
	public void setAdsubject(String adsubject) {
		this.adsubject = adsubject;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public int getHit() {
		return hit;
	}
	public void setHit(int hit) {
		this.hit = hit;
	}
	public String getPpc() {
		return ppc;
	}
	public void setPpc(String ppc) {
		this.ppc = ppc;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getMaj_category() {
		return maj_category;
	}
	public void setMaj_category(String maj_category) {
		this.maj_category = maj_category;
	}
	public String getSub_category() {
		return sub_category;
	}
	public void setSub_category(String sub_category) {
		this.sub_category = sub_category;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	
}
